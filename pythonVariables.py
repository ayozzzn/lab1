# creating variables.
x = 5
y = "John"
print(x)
print(y)

# variables can even change type after they have been set.
x = 4 # x is of type int.
x = "Sally" # x is now of type str.
print(x)

# casting.
x = str(3)    # x will be '3'.
y = int(3)    # y will be 3.
z = float(3)  # z will be 3.0.

# get the type.
x = 5
y = "John"
print(type(x))
print(type(y))

# quotes.
x = "John"
# is the same as
x = 'John'

# case-sensitive.
a = 4
A = "Sally"
print(a)
print(A) #A will not overwrite a.
