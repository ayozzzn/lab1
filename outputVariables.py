#output variables.
x = "Python is awesome"
print(x) # print() function is often used to output variables.

# output multiple variables.
x = "Python"
y = "is"
z = "awesome"
print(x, y, z)

# or use the operator '+'.
x = "Python "
y = "is "
z = "awesome"
print(x + y + z)

# output numbers.
x = 5
y = 10
print(x + y) # for numbers, the '+' character works as a mathematical operator.

# error.
x = 5
y = "John"
print(x + y) # when you try to combine a string and a number, python will give you an error.

# The best way to output multiple variables in the print() function is to separate them with commas, which even support different data types.
x = 5
y = "John"
print(x, y)